#!/usr/bin/env node
/**
 * Generates src/content.generated.ts from the repo-root docs — the single
 * source of truth: ../api.md, ../mcp.md, ../llms.txt.
 *
 * Markdown is converted to HTML at build time and mapped onto the page's
 * existing design: tables wrapped in <div class="tbl">, HTTP-method badges,
 * section pills, headings demoted one level under the hero <h1>.
 *
 * Run: npm run gen   (also runs automatically via `npm run deploy` and CI)
 */
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import { marked } from "marked";

const workerDir = join(dirname(fileURLToPath(import.meta.url)), "..");
const repoRoot = join(workerDir, "..");
const read = (name) => readFileSync(join(repoRoot, name), "utf8");

marked.use({ gfm: true });

/** Render one markdown doc into an on-brand HTML section. */
function renderSection(md, { id, pill }) {
  let html = marked.parse(md);
  // Demote headings one level (deepest first) — the page keeps a single hero <h1>.
  html = html
    .replace(/<(\/?)h4>/g, "<$1h5>")
    .replace(/<(\/?)h3>/g, "<$1h4>")
    .replace(/<(\/?)h2>/g, "<$1h3>")
    .replace(/<(\/?)h1>/g, "<$1h2>");
  // The doc title becomes the section heading: anchor id + brand pill.
  html = html.replace(/<h2>(.*?)<\/h2>/, `<h2 id="${id}">$1 <span class="pill">${pill}</span></h2>`);
  // HTTP-method badges on endpoint headings (e.g. "POST /v1/sites").
  html = html.replace(/<h3>(GET|POST|PUT|PATCH|DELETE)(?= )/g, '<h3><span class="method">$1</span>');
  // Tables scroll inside the card wrapper.
  html = html.replace(/<table>/g, '<div class="tbl"><table>').replace(/<\/table>/g, "</table></div>");
  return html.trim();
}

const LLMS = read("llms.txt");
const BODY =
  renderSection(read("api.md"), { id: "api", pill: "REST" }) +
  "\n\n" +
  renderSection(read("mcp.md"), { id: "mcp", pill: "MCP" });

const out = `/**
 * AUTO-GENERATED by scripts/gen-content.mjs — DO NOT EDIT.
 * Source of truth: api.md, mcp.md, llms.txt at the repo root.
 * Regenerate with \`npm run gen\` (runs automatically on deploy).
 */

/** Verbatim contents of /llms.txt. */
export const LLMS = ${JSON.stringify(LLMS)};

/** Rendered HTML body of the docs page (api + mcp sections). */
export const BODY = ${JSON.stringify(BODY)};
`;

const outPath = join(workerDir, "src", "content.generated.ts");
writeFileSync(outPath, out);
console.log(`wrote ${outPath} (LLMS ${LLMS.length} chars, BODY ${BODY.length} chars)`);
